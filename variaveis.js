// VARIÁVEIS

//Minha primeira com JS variável no Santander Coders
let idade = 27

console.log(idade)

//Tipos de Variáveis
let numero = 30         //Number
let altura = 1.64       //Number
let nome = 'Thayna'     //String
let estudando = true    //Booleano (true or false)

console.log(numero, typeof numero)
console.log(altura)
console.log(nome)
console.log(estudando)

//Outros tipos de variáveis
//Variável 'undefined'
let indefinida = undefined;
console.log(indefinida);

//Variável 'null'
let nula = null;
console.log(nula);

//Sintaxe antiga: 'var' no lugar de 'let'
var variavel = 0
console.log(variavel)

//Variável constante
const constante = 100
console.log(constante)      //Constante tem um único valor e não podemos atribuir um outro valor a ela



