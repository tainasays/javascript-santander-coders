// > OPERADORES ARITMÉTICOS

//Boas práticas para as variáveis

let primeiroNum = 20;
let segundoNum = 2;

//camelSensitive: sempre começa com a letra minúscula

//Adição:            +
//Subtração:         -
//Multiplicação:     *
//Divisão:           /
//Exponenciação:    **
//Resto da Divisão: %

console.log('Soma =', primeiroNum + segundoNum);
console.log('Subtração =', primeiroNum - segundoNum);
console.log('Multiplicação =', primeiroNum * segundoNum);
console.log('Divisão =', primeiroNum / segundoNum);
console.log('Exponenciação =', primeiroNum ** segundoNum);
console.log('Resto =', primeiroNum % segundoNum);
console.log('Resto =', primeiroNum % 3);

//Precedência dos operadores

